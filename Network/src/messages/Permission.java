package messages;

public enum Permission{
	/**
	 * adds permissions for debugging purposes
	 */
	DEBUGGING, 
	
	/**
	 * standard permissions
	 */
	STANDARD, 
	
	/**
	 * adds player permissions
	 */
	PLAYER, 
	
	/**
	 * adds admin permissions
	 */
	ADMIN,
	
	/**
	 * control the play back of the server
	 */
	PLAYBACK;
}
